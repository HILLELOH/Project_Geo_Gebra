
class Rect():
    def __init__(self, a_x, a_y, b_x, b_y):
        self.a_x = a_x
        self.a_y = a_y
        self.b_x = b_x
        self.b_y = b_y

    def get_a_x(self):
        return self.a_x

    def get_a_y(self):
        return self.a_y

    def get_b_x(self):
        return self.b_x

    def get_b_y(self):
        return self.b_y



